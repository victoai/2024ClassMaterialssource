let food = [ "짜장면",  "피자" , "치킨" ] ;
//shift
//배열의 맨앞의 요소를 제거하고 제거된 요소를 반환
let removedItemF = food.shift();
console.log( food);
console.log( removedItemF);



//unshift 
//배열의 맨앞에 요소를 추가하고 새로운 배열의 길이를 반환함 


let newLength = food.unshift("갈비찜");
console.log( food);
console.log( newLength);



